#include <stddef.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 == NULL || s2 == NULL || n == 0)
		return (0);
	while (*s1 == *s2 && n > 0)
	{
		if (*s1 == 0)
			return (1);
		s1++;
		s2++;
		n--;
	}
	return ((n == 0) ? 1 : 0);
}
